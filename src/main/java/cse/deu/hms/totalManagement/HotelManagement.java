    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String uniqueCode = (String) jComboBox4.getSelectedItem(); // 고유 문자
        String newId = jTextField7.getText().trim(); // ID 입력 필드에서 값 가져오기
        String newPass = jTextField8.getText().trim(); // PASS 입력 필드에서 값 가져오기

        // 입력값 검증
        if (newId.isEmpty() || newPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "모든 필드를 입력하세요.");
            return;
        }

        try {
            // 중복 검사
            if (accountFile.isIdDuplicate(newId)) {
                JOptionPane.showMessageDialog(this, "이미 존재하는 ID입니다.");
                return;
            }

            // 계정 추가
            accountFile.addAccount(uniqueCode, newId, newPass);

            // 테이블에 추가
            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            model.addRow(new Object[]{uniqueCode, newId, newPass});

            // 성공 메시지
            JOptionPane.showMessageDialog(this, "계정이 추가되었습니다.");

            // 입력 필드 초기화
            jTextField7.setText("");
            jTextField8.setText("");

            // 다이얼로그 닫기
            accountAppend.dispose();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "계정 추가 중 오류 발생");
        }
    }//GEN-LAST:event_jButton7ActionPerformed


