    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String menuName = jTextField4.getText().trim(); // 메뉴 이름
        String menuPrice = jTextField3.getText().trim(); // 메뉴 가격
        String menuType = (String) jComboBox2.getSelectedItem(); // 식당/룸서비스 구분

        // 입력값 검증
        if (menuName.isEmpty() || menuPrice.isEmpty() || menuType.isEmpty()) {
            JOptionPane.showMessageDialog(this, "모든 필드를 입력하세요.");
            return;
        }

        // 중복 검사
        try {
            if (menuFile.isMenuDuplicate(menuName, menuType)) {
                JOptionPane.showMessageDialog(this, "이미 존재하는 메뉴입니다.");
                return;
            }

            // 파일에 추가
            menuFile.addMenu(menuName, menuPrice, menuType);

            // 테이블에 추가
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.addRow(new Object[]{menuName, menuPrice, menuType});

            // 입력 필드 초기화
            jTextField4.setText("");
            jTextField3.setText("");

            // 성공 메시지
            JOptionPane.showMessageDialog(this, "메뉴가 성공적으로 추가되었습니다.");

            // 다이얼로그 닫기
            menuAppend.dispose();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "메뉴 추가 중 오류 발생: " + e.getMessage(), "오류", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed


